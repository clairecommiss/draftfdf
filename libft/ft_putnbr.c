#include "libft.h"

void	ft_putnbr(int n)
{
	if (n == INT_MIN)
	{
		ft_putchar('-');
		ft_putstr("2147483648");
	}
	else
	{
		if (n < 0)
		{
			ft_putchar('-');
			n = n * (-1);
		}
		if (n > 9)
			ft_putnbr(n / 10);
		ft_putchar(n % 10 + 48);
	}
}
